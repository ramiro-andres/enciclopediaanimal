def normalize(text)
  text.to_s.downcase.unicode_normalize(:nfd).gsub(/\p{M}/, '').strip
end

def build_index
  terms = {}
  by_category = {}

  CATEGORY_SYNONYMS.each do |category, pairs|
    cat_terms = {}
    pairs.each do |canonical, synonyms|
      canonical_n = normalize(canonical)
      syns = synonyms.map { |s| normalize(s) }.uniq
      cat_terms[canonical_n] = syns
      terms[canonical_n] ||= []
      terms[canonical_n] = (terms[canonical_n] + syns).uniq
    end
    by_category[category] = cat_terms
  end

  {
    'version' => 1,
    'generated_at' => Time.now.utc.strftime('%Y-%m-%dT%H:%M:%SZ'),
    'terms' => terms,
    'by_category' => by_category,
    'stats' => {
      'categories' => by_category.length,
      'canonical_terms' => terms.length,
      'synonym_entries' => terms.values.flatten.uniq.length
    }
  }
end

def validate!(data)
  raise 'Debe haber al menos 10 categorías' if data['by_category'].length < 10

  data['by_category'].each do |cat, pairs|
    raise "Categoría #{cat} con menos de 30 términos (#{pairs.length})" if pairs.length < 30
  end
end

if __FILE__ == $PROGRAM_NAME
  data = build_index
  validate!(data)
  File.write(JSON_PATH, JSON.pretty_generate(data))
  File.write(JS_PATH, "window.SEARCH_SYNONYMS = #{data.to_json};\n")
  puts "search_synonyms.json actualizado (#{data['stats']['canonical_terms']} términos, #{data['stats']['categories']} categorías)"
end
